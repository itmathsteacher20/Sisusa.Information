namespace Sisusa.Information.Identification;

/// <summary>
/// Represents the gender of an individual.
/// </summary>
public enum Gender : ushort
{
    /// <summary>
    /// Female gender.
    /// </summary>
    Female = 1,

    /// <summary>
    /// Male gender.
    /// </summary>
    Male = 2,

    /// <summary>
    /// Unknown or unspecified gender.
    /// </summary>
    Unknown = 3
}
